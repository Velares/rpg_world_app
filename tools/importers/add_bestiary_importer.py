def is_add_bestiary_entry_page(actual_page: int) -> bool:
    start_page, end_page = ADD_BESTIARY_ENTRY_PAGE_RANGE
    return start_page <= actual_page <= end_page


def canonical_label(label: str) -> str:
    normalized = " ".join(label.strip().split()).casefold()
    for item in ALL_LABELS:
        if item.casefold() == normalized:
            return item
    return label.strip()


def extract_pdf_pages(pdf_path: Path) -> list[ImportPage]:
    try:
        from pypdf import PdfReader
    except ImportError as exc:  # pragma: no cover
        raise RuntimeError(
            "The ADD Bestiary importer requires pypdf for PDF extraction. "
            "Use the bundled Codex runtime Python or install pypdf in the environment used for this command."
        ) from exc

    reader = PdfReader(str(pdf_path))
    pages: list[ImportPage] = []
    for actual_page, page in enumerate(reader.pages, start=1):
        if not is_add_bestiary_entry_page(actual_page):
            continue
        pages.append(
            ImportPage(
                actual_page=actual_page,
                book=1,
                text=page.extract_text() or "",
            )
        )
    return pages


def write_raw_pages(pages: list[ImportPage], raw_pages_dir: Path) -> None:
    raw_pages_dir.mkdir(parents=True, exist_ok=True)
    for page in pages:
        output_path = raw_pages_dir / f"page_{page.actual_page:03d}.txt"
        output_path.write_text(
            page.text.replace("\r\n", "\n").replace("\r", "\n"),
            encoding="utf-8",
        )


def detect_printed_page_number(page_text: str) -> int | None:
    for raw_line in page_text.replace("\r\n", "\n").replace("\r", "\n").splitlines():
        line = normalize_line(raw_line)
        if PRINTED_PAGE_PATTERN.fullmatch(line):
            try:
                return int(line)
            except ValueError:
                return None
    return None


def cleaned_page_lines(page: ImportPage) -> list[str]:
    lines: list[str] = []
    for raw_line in page.text.replace("\r\n", "\n").replace("\r", "\n").splitlines():
        line = normalize_line(raw_line)
        if not line or is_page_marker_line(line):
            continue
        lines.append(line)
    return lines


def looks_like_title_line(line: str) -> bool:
    if not line or ":" in line or SKIP_HEADING_PATTERN.match(line):
        return False
    if line.endswith(".") or len(line) > 80:
        return False
    words = TITLE_WORD_PATTERN.findall(line)
    if not words or len(words) > 10:
        return False
    title_like = 0
    for word in words:
        stripped = word.strip("()[],.")
        if not stripped:
            continue
        if stripped.isupper():
            title_like += 1
        elif stripped[0].isupper():
            title_like += 1
        elif stripped.casefold() in {"of", "the", "and", "as", "or", "to", "in"}:
            title_like += 1
    return title_like >= max(1, len(words) - 1)


def find_label_positions(lines: list[str]) -> list[tuple[int, str, str]]:
    positions: list[tuple[int, str, str]] = []
    for index, line in enumerate(lines):
        match = LABEL_PATTERN.match(line)
        if not match:
            continue
        positions.append((index, canonical_label(match.group(1)), match.group(2).strip()))
    return positions


def split_entries_from_pages(pages: list[ImportPage]) -> tuple[list[AddBestiaryEntry], list[int]]:
    line_records: list[tuple[int, str]] = []
    raw_pages_by_number: dict[int, str] = {page.actual_page: page.text for page in pages}
    pages_with_titles: set[int] = set()
    for page in pages:
        lines = cleaned_page_lines(page)
        for line in lines:
            if looks_like_title_line(line):
                pages_with_titles.add(page.actual_page)
            line_records.append((page.actual_page, line))

    entries: list[AddBestiaryEntry] = []
    start_indexes: list[int] = []
    for index, (_, line) in enumerate(line_records):
        if not looks_like_title_line(line):
            continue
        lookahead = [candidate for _, candidate in line_records[index + 1 : index + 25]]
        if any(LABEL_PATTERN.match(candidate) for candidate in lookahead):
            start_indexes.append(index)

    for offset, start_index in enumerate(start_indexes):
        end_index = start_indexes[offset + 1] if offset + 1 < len(start_indexes) else len(line_records)
        chunk = line_records[start_index:end_index]
        if not chunk:
            continue
        actual_start = chunk[0][0]
        actual_end = chunk[-1][0]
        printed_page = detect_printed_page_number(raw_pages_by_number.get(actual_start, ""))
        entries.append(
            AddBestiaryEntry(
                heading=chunk[0][1],
                actual_page_start=actual_start,
                actual_page_end=actual_end,
                printed_page=printed_page,
                raw_text="\n".join(line for _, line in chunk),
            )
        )
    pages_without_records = sorted(
        page.actual_page
        for page in pages
        if page.actual_page not in pages_with_titles
    )
    return entries, pages_without_records


def parse_labeled_block(raw_text: str) -> tuple[dict[str, str], dict[str, str], Counter[str]]:
    lines = [line for line in raw_text.replace("\r\n", "\n").replace("\r", "\n").splitlines() if normalize_line(line)]
    label_positions = find_label_positions(lines)
    field_counts = Counter(label for _, label, _ in label_positions)
    stat_block: dict[str, str] = {}
    sections: dict[str, str] = {}
    for position_index, (line_index, label, remainder) in enumerate(label_positions):
        next_line_index = (
            label_positions[position_index + 1][0]
            if position_index + 1 < len(label_positions)
            else len(lines)
        )
        value_lines = [remainder] if remainder else []
        value_lines.extend(lines[line_index + 1 : next_line_index])
        value = " ".join(normalize_line(line) for line in value_lines if normalize_line(line)).strip()
        if label in STAT_LABEL_SET:
            stat_block[label] = value
        elif label in PROSE_LABEL_SET:
            sections[label] = value
    return stat_block, sections, field_counts


def infer_safe_tags(name: str, stat_block: dict[str, str], sections: dict[str, str]) -> list[str]:
    haystack = " ".join(
        filter(
            None,
            [
                name,
                stat_block.get("Move", ""),
                stat_block.get("Size", ""),
                sections.get("General", ""),
                sections.get("Appearance", ""),
            ],
        )
    ).casefold()
    tags: list[str] = []
    for tag, needles in SAFE_TAGS.items():
        if any(needle in haystack for needle in needles):
            tags.append(tag)
    return sorted(set(tags))


def infer_type(name: str) -> str | None:
    lowered = name.casefold()
    if "demon" in lowered or "devil" in lowered:
        return "fiend"
    if "dragon" in lowered:
        return "dragon"
    if "golem" in lowered or "animated" in lowered:
        return "construct"
    if "zombie" in lowered or "ghoul" in lowered or "wight" in lowered:
        return "undead"
    if "giant" in lowered:
        return "giant"
    return None


def build_monster_record(entry: AddBestiaryEntry, stat_block: dict[str, str], sections: dict[str, str]) -> dict[str, Any]:
    display_name = title_case_name(entry.heading)
    monster_id = f"add_bestiary.monster.{slugify_name(display_name)}"
    return {
        "id": monster_id,
        "name": display_name,
        "sort_name": entry.heading,
        "source": {
            "file": DEFAULT_ADD_BESTIARY_PDF.name,
            "name": SOURCE_PACK_NAME,
            "type": SOURCE_TYPE,
            "actual_page_start": entry.actual_page_start,
            "actual_page_end": entry.actual_page_end,
        },
        "source_pack": SOURCE_PACK_ID,
        "source_page_actual": entry.actual_page_start,
        "source_page_printed": entry.printed_page,
        "stat_block": stat_block,
        "sections": sections,
        "type": infer_type(display_name),
        "habitats": [],
        "regions": [],
        "tags": infer_safe_tags(display_name, stat_block, sections),
        "import_notes": [],
        "raw_text": entry.raw_text,
    }


def classify_entry(entry: AddBestiaryEntry) -> tuple[dict[str, Any] | None, RejectedAddBestiaryEntry | None, list[str]]:
    stat_block, sections, field_counts = parse_labeled_block(entry.raw_text)
    missing_fields = [field_name for field_name in REQUIRED_STAT_FIELDS if not stat_block.get(field_name)]
    notes: list[str] = []
    if any(field_counts.get(field_name, 0) > 1 for field_name in REQUIRED_STAT_FIELDS):
        return (
            None,
            RejectedAddBestiaryEntry(
                heading=entry.heading,
                actual_page_start=entry.actual_page_start,
                actual_page_end=entry.actual_page_end,
                reason="multi_variant_stat_block_not_supported_yet",
                raw_text=entry.raw_text,
            ),
            notes,
        )
    if missing_fields:
        return (
            None,
            RejectedAddBestiaryEntry(
                heading=entry.heading,
                actual_page_start=entry.actual_page_start,
                actual_page_end=entry.actual_page_end,
                reason=f"missing_required_stat_fields: {', '.join(missing_fields)}",
                raw_text=entry.raw_text,
            ),
            notes,
        )
    if not any(sections.get(field_name) for field_name in ("General", "Combat", "Appearance")):
        return (
            None,
            RejectedAddBestiaryEntry(
                heading=entry.heading,
                actual_page_start=entry.actual_page_start,
                actual_page_end=entry.actual_page_end,
                reason="missing_required_prose_section",
                raw_text=entry.raw_text,
            ),
            notes,
        )
    monster = build_monster_record(entry, stat_block, sections)
    return monster, None, notes


def validate_imported_monster_pack(
    pack_json_path: Path,
    monsters_json_path: Path,
) -> list[str]:
    errors: list[str] = []
    if not pack_json_path.exists():
        return [f"Missing pack metadata file: {pack_json_path}"]
    if not monsters_json_path.exists():
        return [f"Missing monsters file: {monsters_json_path}"]
    pack = json.loads(pack_json_path.read_text(encoding="utf-8"))
    payload = json.loads(monsters_json_path.read_text(encoding="utf-8"))
    monsters = payload.get("monsters", []) if isinstance(payload, dict) else payload
    if not isinstance(monsters, list):
        return ["monsters.json must contain a list or a top-level monsters list."]
    seen_ids: set[str] = set()
    for index, monster in enumerate(monsters, start=1):
        if not isinstance(monster, dict):
            errors.append(f"Monster {index} is not an object.")
            continue
        for field_name in ("id", "name", "source", "source_page_actual", "stat_block"):
            if field_name not in monster or monster.get(field_name) in (None, "", []):
                errors.append(f"Monster {index} missing required field: {field_name}")
        monster_id = str(monster.get("id") or "")
        if monster_id:
            if monster_id in seen_ids:
                errors.append(f"Duplicate monster id: {monster_id}")
            seen_ids.add(monster_id)
        stat_block = monster.get("stat_block", {})
        if isinstance(stat_block, dict):
            for field_name in REQUIRED_STAT_FIELDS:
                if not stat_block.get(field_name):
                    errors.append(f"{monster_id or monster.get('name')}: missing ADD Bestiary field {field_name}")
        else:
            errors.append(f"{monster_id or monster.get('name')}: stat_block must be an object")
    if pack.get("id") != SOURCE_PACK_ID:
        errors.append(f"pack.json id must be {SOURCE_PACK_ID}")
    return errors


