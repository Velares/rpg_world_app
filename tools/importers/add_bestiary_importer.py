def build_import_report(
    *,
    source_info: ResolvedMonsterSource,
    pack_json_path: Path,
    monsters_json_path: Path,
    raw_pages_dir: Path,
    report_path: Path,
    scanned_pages: list[int],
    skipped_pages: list[str],
    monsters: list[dict[str, Any]],
    rejected_entries: list[RejectedAddBestiaryEntry],
    duplicate_ids: list[str],
    same_name_warnings: list[str],
    missing_field_errors: list[str],
    pages_without_records: list[int],
) -> str:
    partial_count = sum(
        1
        for entry in rejected_entries
        if entry.reason == "multi_variant_stat_block_not_supported_yet"
    )
    lines = [
        "Adventures Dark and Deep Bestiary Import Report",
        "===============================================",
        "",
        "Importer: add_bestiary_importer",
        f"Source ID: {source_info.source_id or 'None'}",
        f"Source title: {source_info.source_title or 'None'}",
        f"Source status: {source_info.source_status or 'None'}",
        f"Source path used: {source_info.source_path}",
        f"Source path mode: {'direct override' if source_info.used_path_override else 'registry/default'}",
        f"Registered expected path: {source_info.path_display}",
        f"Input file present: {'yes' if source_info.exists else 'no'}",
        f"Pack output path: {pack_json_path}",
        f"Monsters output path: {monsters_json_path}",
        f"Raw pages directory: {raw_pages_dir}",
        f"Report output path: {report_path}",
        "",
        f"Actual PDF pages scanned: {scanned_pages[0]}-{scanned_pages[-1] if scanned_pages else 0}",
        f"Pages skipped: {', '.join(skipped_pages)}",
        f"Accepted monster count: {len(monsters)}",
        f"Rejected candidate count: {len(rejected_entries)}",
        f"Partial/unsupported multi-variant count: {partial_count}",
        f"Duplicate ID count: {len(duplicate_ids)}",
        f"Same-name/different-source warnings: {len(same_name_warnings)}",
        f"Missing field errors: {len(missing_field_errors)}",
        "",
        "Reminder: actual PDF pages were used, not printed page numbers.",
        "",
        "Same-name/different-source warnings:",
    ]
    lines.extend(f"- {warning}" for warning in same_name_warnings) if same_name_warnings else lines.append("- None")
    lines.extend(["", "Missing field errors:"])
    lines.extend(f"- {item}" for item in missing_field_errors) if missing_field_errors else lines.append("- None")
    lines.extend(["", "Duplicate IDs:"])
    lines.extend(f"- {item}" for item in duplicate_ids) if duplicate_ids else lines.append("- None")
    lines.extend(["", "Rejected records:"])
    if rejected_entries:
        lines.extend(
            f"- Page {entry.actual_page_start} [{entry.heading}]: {entry.reason}"
            for entry in rejected_entries
        )
    else:
        lines.append("- None")
    lines.extend(["", "Pages where no monster records were found:"])
    if pages_without_records:
        lines.append("- " + ", ".join(str(page) for page in pages_without_records))
    else:
        lines.append("- None")
    lines.extend(["", "Accepted monster examples:"])
    if monsters:
        lines.extend(
            f"- Page {monster['source_page_actual']}: {monster['name']} ({monster['id']})"
            for monster in monsters[:10]
        )
    else:
        lines.append("- None")
    lines.extend(
        [
            "",
            "Next recommended action:",
            f"- Review {report_path.name} before promoting this import pack into wider catalog workflows.",
            f"- Inspect raw page extracts under {raw_pages_dir} when a candidate entry is rejected.",
        ]
    )
    return "\n".join(lines) + "\n"


def build_import_from_pages(
    pages: list[ImportPage],
    *,
    source_info: ResolvedMonsterSource,
    existing_catalog_path: Path = DEFAULT_MONSTER_CATALOG_JSON,
    pack_json_path: Path = DEFAULT_ADD_BESTIARY_PACK_JSON,
    monsters_json_path: Path = DEFAULT_ADD_BESTIARY_MONSTERS_JSON,
    raw_pages_dir: Path = DEFAULT_ADD_BESTIARY_RAW_PAGES_DIR,
    report_path: Path = DEFAULT_ADD_BESTIARY_IMPORT_REPORT,
) -> AddBestiaryImportResult:
    entries, pages_without_records = split_entries_from_pages(pages)
    accepted_monsters: list[dict[str, Any]] = []
    rejected_entries: list[RejectedAddBestiaryEntry] = []
    missing_field_errors: list[str] = []

    for entry in entries:
        monster, rejected, _notes = classify_entry(entry)
        if rejected is not None:
            rejected_entries.append(rejected)
            if rejected.reason.startswith("missing_required_stat_fields"):
                missing_field_errors.append(
                    f"Page {rejected.actual_page_start} [{rejected.heading}]: {rejected.reason}"
                )
            continue
        if monster is not None:
            accepted_monsters.append(monster)

    duplicate_ids = sorted(
        monster_id
        for monster_id, count in Counter(monster["id"] for monster in accepted_monsters).items()
        if count > 1
    )
    if duplicate_ids:
        filtered: list[dict[str, Any]] = []
        seen_ids: set[str] = set()
        for monster in accepted_monsters:
            if monster["id"] in seen_ids:
                rejected_entries.append(
                    RejectedAddBestiaryEntry(
                        heading=monster["name"],
                        actual_page_start=monster["source_page_actual"],
                        actual_page_end=monster["source_page_actual"],
                        reason="duplicate_id_in_source",
                        raw_text=monster.get("raw_text", ""),
                    )
                )
                continue
            seen_ids.add(monster["id"])
            filtered.append(monster)
        accepted_monsters = filtered

    existing_records = load_all_monster_records(
        catalog_path=existing_catalog_path,
        content_packs_root=DEFAULT_ADD_BESTIARY_PACK_JSON.parents[1],
    )
    same_name_warnings = find_same_name_different_source_records(existing_records + accepted_monsters)

    pack = build_pack_metadata(source_info)
    monsters_payload = build_monsters_payload(
        accepted_monsters,
        source_info.source_path,
        source_info,
    )
    report_text = build_import_report(
        source_info=source_info,
        pack_json_path=pack_json_path,
        monsters_json_path=monsters_json_path,
        raw_pages_dir=raw_pages_dir,
        report_path=report_path,
        scanned_pages=[page.actual_page for page in pages],
        skipped_pages=["1-3", "439+"],
        monsters=accepted_monsters,
        rejected_entries=rejected_entries,
        duplicate_ids=duplicate_ids,
        same_name_warnings=same_name_warnings,
        missing_field_errors=missing_field_errors,
        pages_without_records=pages_without_records,
    )
    return AddBestiaryImportResult(
        pack=pack,
        monsters_payload=monsters_payload,
        report_text=report_text,
        accepted_count=len(accepted_monsters),
        rejected_count=len(rejected_entries),
        partial_count=sum(
            1 for entry in rejected_entries if entry.reason == "multi_variant_stat_block_not_supported_yet"
        ),
        duplicate_ids=duplicate_ids,
        same_name_warnings=same_name_warnings,
        missing_field_errors=missing_field_errors,
        rejected_entries=rejected_entries,
        pages_without_records=pages_without_records,
    )


def import_add_bestiary(
    pdf_path: Path = DEFAULT_ADD_BESTIARY_PDF,
    *,
    pack_json_path: Path = DEFAULT_ADD_BESTIARY_PACK_JSON,
    monsters_json_path: Path = DEFAULT_ADD_BESTIARY_MONSTERS_JSON,
    raw_pages_dir: Path = DEFAULT_ADD_BESTIARY_RAW_PAGES_DIR,
    report_path: Path = DEFAULT_ADD_BESTIARY_IMPORT_REPORT,
    existing_catalog_path: Path = DEFAULT_MONSTER_CATALOG_JSON,
    source_id: str = ADD_BESTIARY_SOURCE_ID,
    allow_inactive_source: bool = False,
) -> AddBestiaryImportResult:
    source_info = resolve_registered_monster_source(
        default_source_id=ADD_BESTIARY_SOURCE_ID,
        override_path=pdf_path if pdf_path != DEFAULT_ADD_BESTIARY_PDF else None,
        source_id=source_id,
        allow_inactive_source=allow_inactive_source,
    )
    source_info.source_path.parent.mkdir(parents=True, exist_ok=True)
    pages = extract_pdf_pages(source_info.source_path)
    write_raw_pages(pages, raw_pages_dir)
    result = build_import_from_pages(
        pages,
        source_info=source_info,
        existing_catalog_path=existing_catalog_path,
        pack_json_path=pack_json_path,
        monsters_json_path=monsters_json_path,
        raw_pages_dir=raw_pages_dir,
        report_path=report_path,
    )
    pack_json_path.parent.mkdir(parents=True, exist_ok=True)
    monsters_json_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.parent.mkdir(parents=True, exist_ok=True)
    pack_json_path.write_text(json.dumps(result.pack, indent=2, ensure_ascii=False), encoding="utf-8")
    monsters_json_path.write_text(
        json.dumps(result.monsters_payload, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    report_path.write_text(result.report_text, encoding="utf-8")
    return result


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    try:
        result = import_add_bestiary(
            Path(args.input),
            pack_json_path=Path(args.pack_json),
            monsters_json_path=Path(args.monsters_json),
            raw_pages_dir=Path(args.raw_pages_dir),
            report_path=Path(args.report),
            existing_catalog_path=Path(args.catalog),
            source_id=args.source_id,
            allow_inactive_source=args.allow_inactive_source,
        )
    except (FileNotFoundError, RuntimeError) as exc:
        print(exc)
        return 1

    print(
        f"Accepted {result.accepted_count} monsters. "
        f"Rejected={result.rejected_count}, multi-variant deferred={result.partial_count}."
    )
    print(f"Wrote pack: {args.pack_json}")
    print(f"Wrote monsters: {args.monsters_json}")
    print(f"Wrote report: {args.report}")
    print(f"Wrote raw page text to: {args.raw_pages_dir}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
